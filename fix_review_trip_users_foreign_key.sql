-- SQL to fix the foreign key constraint for review_trip table to reference users
-- Run this in your Supabase SQL editor

-- First, let's check the current foreign key constraint
SELECT 
    tc.constraint_name,
    tc.table_name,
    kcu.column_name,
    ccu.table_name AS foreign_table_name,
    ccu.column_name AS foreign_column_name
FROM information_schema.table_constraints AS tc
JOIN information_schema.key_column_usage AS kcu
    ON tc.constraint_name = kcu.constraint_name
    AND tc.table_schema = kcu.table_schema
JOIN information_schema.constraint_column_usage AS ccu
    ON ccu.constraint_name = tc.constraint_name
    AND ccu.table_schema = tc.table_schema
WHERE tc.constraint_type = 'FOREIGN KEY'
AND tc.table_name = 'review_trip';

-- Drop the existing foreign key constraint
ALTER TABLE review_trip DROP CONSTRAINT IF EXISTS review_trip_client_id_fkey;

-- Add the correct foreign key constraint to reference users table
ALTER TABLE review_trip 
ADD CONSTRAINT review_trip_client_id_fkey 
FOREIGN KEY (client_id) REFERENCES auth.users(id) ON DELETE CASCADE;

-- Verify the new constraint was created
